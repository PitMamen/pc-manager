export default {
  components: {
    // Calendar,
    // addForm,
    // editForm,
  },

  data() {
    return {
      selectedRowKeys: [], // Check here to configure the default column

      statusData: [
        { code: 2, value: '全部' },
        { code: 0, value: '启用' },
        { code: 1, value: '停用' },
      ],
      // 高级搜索 展开/关闭
      advanced: false,
      partChoose: '',
      itemData: [
        { code: 1, isChecked: false },
        { code: 2, isChecked: true },
        // { code: 3, isChecked: true },
        // { code: 4, isChecked: true },
