<template>
  <a-modal
    title="添加人员"
    :width="900"
    :visible="visible"
    :confirmLoading="confirmLoading"
    @ok="handleSubmit"
    @cancel="handleCancel"
  >
    <a-spin :spinning="confirmLoading">
      <div class="add-wrap">
        <div class="add-wrap-left">
          <!-- size="large" -->
          <a-auto-complete
            style="width: 100%; font-size: 14px"
            placeholder="请输入并选择"
            option-label-prop="title"
            @select="onSelect"
            @search="handleSearch"
          >
            <template slot="dataSource">
              <a-select-option v-for="item in autoUsers" :key="item.userId + ''" :title="item.userName">
                {{ item.userName }}
              </a-select-option>
            </template>
          </a-auto-complete>

          <div class="left-num-des">
            <span><span style="color: red">*</span> 选择人员</span>

            <span style="margin-left: 50%"
              >已选人数<span style="color: #1890ff">{{ choseNum }}/{{ deptUsers[0].users.length }}</span> 人</span
            >
          </div>

          <div class="left-num-des">
            <span style="margin-left: 9%; margin-top: 1%">{{ deptUsers[0].deptName }}</span>
            <a-icon style="margin-left: 62%" class="checked" :type="iconType" @click="allClicked" />
            <!-- plus -->
            <!-- <a-icon v-if="!isAllChecked" style="margin-left: 2%" :class="{ checked: isAllChecked }" type="minus" @click="allClickedMi" /> -->
          </div>

          <div class="item-person" v-for="(item, index) in deptUsers[0].users" :key="index" :value="item.id">
            <span style="margin-left: 15%; margin-top: 1%; flex: 1" :class="{ checked: item.isChecked }">{{
              item.userName
            }}</span>
            <a-icon
              style="margin-top: 1%"
              :class="{ checked: item.canAdd }"
              type="plus"
              @click="addPerson(item, index)"
            />
          </div>
        </div>

        <div class="divider-col"></div>

        <div class="add-wrap-right">
          <a-table
            ref="table"
            :pagination="false"
            size="default"
            style="margin-left: 2%; min-height: 450px; overflow-y: auto"
            :columns="columns"
            :data-source="choseUsers"
            :alert="true"
            :rowKey="(record) => record.code"
          >
            <span slot="action" slot-scope="text, record">
              <a-icon
                type="delete"
                @click="deleteChoosed(record)"
                theme="filled"
                style="color: #1890ff; margin-left: 10px"
              />
            </span>
            <span slot="act_num" slot-scope="text, record">
              <a-input-number
                style="display: inline-block; margin-left: 1%; width: 100px"
                v-model="record.weight"
                @change="countTotal"
                :min="0"
                :max="10000"
                :maxLength="30"
                allow-clear
                placeholder="请输入数量"
              />
            </span>
          </a-table>
          <div class="right-bottom">
            <div class="right-bottom-left">
              <span>平均分配权重：</span><a-switch :checked="isAverage" @click="goOpen" />
            </div>
            <span>权重合计：{{ totolAverage + '' }}%</span>
          </div>
        </div>
      </div>
    </a-spin>
  </a-modal>
</template>


<script>
import { getPlatTypeList } from '@/api/modular/system/posManage'
export default {
  data() {
    return {
      index: -1,
      chooseName: '',
      confirmLoading: false,
      isAverage: false,
      isSingle: false,
      isAllChecked: false,
      iconType: 'plus',
      choseNum: 0,
      totolAverage: 0,
      deptUsers: [{ deptName: '', users: [] }],
      choseUsers: [],
      autoUsers: [],
      visible: false,
      columns: [
        {
          title: '序号',
          width: 60,
          // innerHeight:20,
          dataIndex: 'xh',
        },
        {
          title: '姓名',
          width: 100,
          dataIndex: 'userName',
        },
        {
          title: '分配权重',
          width: 100,
          scopedSlots: { customRender: 'act_num' },
        },
        {
          title: '操作',
          width: 100,
          dataIndex: 'action',
          scopedSlots: { customRender: 'action' },
        },
      ],
    }
  },
  methods: {
    /**
     * 初始化方法
     *  "deptUsers": [
      {
        "deptName": "精神科门诊",
        "users": [
          {
            "userId": "608",
            "userName": "护士001"
          },
          {
            "userId": "667",
            "userName": "护士002"
          }
        ]
      }
    ]
     * 
     * @param {*} index 
     * @param {*} deptUsers 
     */
    add(index, deptUsers, assignments, isSingle) {
      this.index = index
      this.visible = true
      this.isAverage = false

      if (assignments && assignments.length > 0 && deptUsers[0].users.length == assignments.length) {
        this.iconType = 'minus'
      } else {
        this.iconType = 'plus'
      }

      this.deptUsers = deptUsers
      this.isSingle = isSingle
      this.choseUsers = []

      console.log('before', JSON.parse(JSON.stringify(this.deptUsers)))
      this.deptUsers[0].users.forEach((item) => {
        this.$set(item, 'isChecked', false)
        this.$set(item, 'canAdd', true)
        if (assignments && assignments.length > 0) {
          assignments.forEach((itemAss) => {
            if (itemAss.userId == item.userId) {
              //组装可以添加的用户
              this.$set(item, 'canAdd', false)

              //组装已添加用户
              let tempItem = JSON.parse(JSON.stringify(item))
              this.$set(tempItem, 'weight', itemAss.weight)
              this.choseUsers.push(tempItem)
            }
          })
        }
        // this.$set(item, 'weight', 0)
      })
      this.choseNum = this.choseUsers.length
      this.autoUsers = JSON.parse(JSON.stringify(this.deptUsers[0].users))
      // debugger
      this.countTotal()
      // console.log('after', JSON.parse(JSON.stringify(this.deptUsers)))
    },

    allClicked() {
      if (this.isSingle) {
        this.$message.error('指定人员仅需添加一个')
        return
      }

      this.isAllChecked = !this.isAllChecked

      if (this.isAllChecked) {
        this.iconType = 'minus'

        this.choseUsers = []
        this.deptUsers[0].users.forEach((item) => {
          item.isChecked = true
          item.canAdd = false
          let tempItem = JSON.parse(JSON.stringify(item))
          this.$set(tempItem, 'weight', 0)
          this.choseUsers.push(tempItem)
        })
      } else {
        this.iconType = 'plus'
        this.choseUsers = []
        this.deptUsers[0].users.forEach((item) => {
          item.isChecked = false
          item.canAdd = true
        })
      }

      this.sortChoseUsers()
      this.choseNum = this.choseUsers.length
    },

    goOpen() {
      this.isAverage = !this.isAverage
      if (this.isAverage) {
        let num = (100 / this.choseUsers.length).toFixed(0)
        //平均的时候先设均值，然后改变最后一个值
        this.choseUsers.forEach((item, index) => {
          this.$set(item, 'weight', num)
        })
        this.choseUsers[this.choseUsers.length - 1].weight = 100 - num * (this.choseUsers.length - 1)
        this.totolAverage = 100
      }
    },

    handleSearch(inputName) {
      if (inputName) {
        this.autoUsers = this.deptUsers[0].users.filter((item) => item.userName.indexOf(inputName) != -1)
      } else {
        this.autoUsers = JSON.parse(JSON.stringify(this.deptUsers[0].users))
        // this.chooseDeptItem = { departmentName: '', departmentId: '' }
      }
    },

    onSelect(userId) {
      console.log('userId', userId)
      // console.log('s2', s2) s2为系统参数
      this.deptUsers[0].users.forEach((item, index) => {
        if (item.userId == userId) {
          this.addPerson(item, index)
        }
      })
    },

    /**
     * 左侧添加按钮，左边列表isChecked = true , isDisabled = true
     * @param {} item
     * @param {*} index
     */
    addPerson(item, index) {
      // debugger
      if (!item.canAdd) {
        return
      }

      if (this.isSingle && this.choseUsers.length == 1) {
        this.$message.error('指定人员仅需添加一个')
        return
      }

      item.isChecked = true
      item.canAdd = false
      let tempItem = JSON.parse(JSON.stringify(item))
      this.$set(tempItem, 'weight', 0)
      this.choseUsers.push(tempItem)
      this.sortChoseUsers()
      this.choseNum = this.choseUsers.length
    },

    deleteChoosed(record) {
      this.choseUsers.splice(this.choseUsers.indexOf(record), 1)
      this.deptUsers[0].users.forEach((item) => {
        if (item.userId == record.userId) {
          // debugger
          item.isChecked = false
          item.canAdd = true
        }
      })
      this.sortChoseUsers()
      this.choseNum = this.choseUsers.length

      this.countTotal()
    },

    sortChoseUsers() {
      this.choseUsers.forEach((item, index) => {
        this.$set(item, 'xh', index + 1)
      })
    },

    countTotal() {
      this.totolAverage = 0
      for (let index = 0; index < this.choseUsers.length; index++) {
        this.totolAverage = parseInt(this.totolAverage) + parseInt(this.choseUsers[index].weight)
      }
    },

    handleSubmit() {
      this.countTotal()

      if (this.choseNum == 0) {
        this.$message.error('请选择人员')
        return
      }

      if (this.totolAverage < 98) {
        this.$message.error('总权重过小')
        return
      }

      if (this.totolAverage > 100) {
        this.$message.error('总权重过大')
        return
      }

      for (let index = 0; index < this.choseUsers.length; index++) {
        if (this.choseUsers[index].weight == 0) {
          this.$message.error(this.choseUsers[index].userName + '权重为0')
          return
        }
      }
      console.log('this.choseUsers', this.choseUsers)
      let proccesedAssignments = JSON.parse(JSON.stringify(this.choseUsers))
      proccesedAssignments.forEach((item, index) => {
        delete item.canAdd
        delete item.isChecked
        // delete item.userName
        delete item.xh
      })
      console.log('this.proccesedAssignments', proccesedAssignments)
      this.$emit('ok', this.index, proccesedAssignments)
      this.visible = false
    },
    handleCancel() {
      // this.form.resetFields()
      this.visible = false
    },
  },
}
</script>
